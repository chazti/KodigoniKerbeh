    private void updateCart(long longganisa){
        if(!cart.contains(longganisa)){
            cart.add(longganisa);
        }else{
            JOptionPane.showMessageDialog(this, "Product already scanned!");
        }
        displayCart();
    }
    
    private void displayCart(){
        jTextAreaScanned.setText("");
        for (int i = 0; i < cart.size(); i++) {
            jTextAreaScanned.setText(jTextAreaScanned.getText() + cart.get(i).toString() + "\n");
        }
    }
    
